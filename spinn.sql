--
-- Estructura para la vista `view_frp_articulo`
--
DROP TABLE IF EXISTS `view_frp_articulo`;

CREATE ALGORITHM=UNDEFINED DEFINER=`root`@`localhost` SQL SECURITY DEFINER VIEW `view_frp_articulo`  AS  select `t0`.`IdFRP` AS `IdFRP`,`t0`.`Cantidad` AS `Cantidad`,`t0`.`IdArticulo` AS `IdArticulo`,`t0`.`Descripcion` AS `Descripcion`,((select sum(`t1`.`Puntos`) from `detallefrp` `t1` where ((`t1`.`IdFRP` = `t0`.`IdFRP`) and (`t1`.`IdArticulo` = `t0`.`IdArticulo`))) / `t0`.`Cantidad`) AS `Puntos`,(select sum(`t1`.`Puntos`) from `detallefrp` `t1` where ((`t1`.`IdFRP` = `t0`.`IdFRP`) and (`t1`.`IdArticulo` = `t0`.`IdArticulo`))) AS `Total` from `detallefrp` `t0` group by `t0`.`IdFRP`,`t0`.`IdArticulo` ;

